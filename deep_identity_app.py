def get_openai_client() -> Optional[OpenAI]:
    """
    –ü—ã—Ç–∞–µ–º—Å—è –∞–∫–∫—É—Ä–∞—Ç–Ω–æ –ø–æ–ª—É—á–∏—Ç—å OpenAI-–∫–ª–∏–µ–Ω—Ç:
    1) st.secrets["OPENAI_API_KEY"]
    2) env OPENAI_API_KEY
    –ï—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ—Ç ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º None (–Ω–µ –ø–∞–¥–∞–µ–º).
    """
    if OpenAI is None:
        return None

    api_key = None
    try:
        api_key = st.secrets.get("OPENAI_API_KEY", None)
    except Exception:
        api_key = None

    if not api_key:
        api_key = os.environ.get("OPENAI_API_KEY")

    if not api_key:
        return None

    return OpenAI(api_key=api_key)



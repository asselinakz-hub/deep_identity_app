def run_block(block_num: int, questions: List[Dict[str, Any]]):
    st.subheader(f"–ë–ª–æ–∫ {block_num}")

    col_map = {1: "c1", 2: "c2", 3: "c3"}
    target_col = col_map[block_num]

    # –†–µ–Ω–¥–µ—Ä–∏–º –≤–æ–ø—Ä–æ—Å—ã
    for idx, q in enumerate(questions, start=1):
        st.markdown(f"**{idx}. {q['text']}**")

        # –†–∞–¥–∏–æ
        radio_key = f"block{block_num}_{q['id']}_choice"
        option_labels = [opt["label"] for opt in q["options"]]
        default_index = 0

        # –µ—Å–ª–∏ —É–∂–µ –±—ã–ª –≤—ã–±–æ—Ä, –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏–º
        if radio_key in st.session_state:
            try:
                default_index = option_labels.index(st.session_state[radio_key])
            except ValueError:
                default_index = 0

        choice = st.radio(
            "–í—ã–±–µ—Ä–∏ –æ–¥–∏–Ω –≤–∞—Ä–∏–∞–Ω—Ç:",
            options=option_labels,
            index=default_index,
            key=radio_key,
        )

        # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π —Ç–µ–∫—Å—Ç
        text_key = f"block{block_num}_{q['id']}_text"
        st.text_area(
            "–†–∞—Å—Å–∫–∞–∂–∏ –ø–æ–¥—Ä–æ–±–Ω–µ–µ (–ø–æ –∂–µ–ª–∞–Ω–∏—é):",
            key=text_key,
            height=80,
        )

        st.markdown("---")

    # –ö–Ω–æ–ø–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –±–ª–æ–∫–∞
    if st.button(f"üíæ –°–æ—Ö—Ä–∞–Ω–∏—Ç—å –ë–ª–æ–∫ {block_num}"):
        # –û–±–Ω—É–ª—è–µ–º —Ç–∞–±–ª–∏—Ü—É –¥–ª—è –±–ª–æ–∫–∞
        block_cols = {p: {c: 0 for c in COLUMNS} for p in POTENTIALS}

        # –°–æ–±–∏—Ä–∞–µ–º —Ç–µ–∫—Å—Ç
        full_text_parts: List[str] = []

        for q in questions:
            radio_key = f"block{block_num}_{q['id']}_choice"
            text_key = f"block{block_num}_{q['id']}_text"

            chosen_label = st.session_state.get(radio_key, None)
            comment = st.session_state.get(text_key, "").strip()

            # –ù–∞—Ö–æ–¥–∏–º –∫–∞–∫–∏–µ –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª—ã —É—Å–∏–ª–∏–≤–∞—Ç—å
            if chosen_label is not None:
                for opt in q["options"]:
                    if opt["label"] == chosen_label:
                        pots = opt.get("pots", [])
                        for pot in pots:
                            if pot in block_cols:
                                block_cols[pot][target_col] += 1

                        break

            # –¢–µ–∫—Å—Ç
            if comment:
                full_text_parts.append(f"[{q['id']}] {q['text']}\n{comment}\n")

        st.session_state[f"block{block_num}_cols"] = block_cols
        st.session_state[f"block{block_num}_text"] = "\n".join(full_text_parts).strip()

        st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ë–ª–æ–∫–∞ {block_num} —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã ‚úÖ")


